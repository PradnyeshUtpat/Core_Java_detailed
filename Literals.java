public class Literals {
    public static void main(String[] args) {
        int num = 10_00_000;
        double num1 = 1e11;
        int num2 = 0b101;
        int num3 = 0xFFF;
        char c = 'a';
        c++;
        System.out.println(c);
        System.out.println(num);
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
    }
}
