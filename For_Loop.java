public class For_Loop {
    public static void main(String[] args) {
        System.out.println("Hello this is a for loop");
        for (int index = 1; index <= 7; index++) {
            System.out.println("DAY : " + index);
            for(int i = 1 ; i <=24 ; i++ ){
                System.out.println("HOUR : " + i);
            }
        }
    }   
}
